    //產生冰塊
    void Icecreate()
    {
        for (int y = 0; y < 16; y++)
        {
            for (int x = 0; x < 16; x++)
            {
                if (IceMap[y, x] == 1)
                {
                    Instantiate(ice, GridToPosition(x, y), Quaternion.identity);
                }
            }
        }
    }

